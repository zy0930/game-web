import { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || "https://gameweb.com";

  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: [
          "/api/*",
          "/account/*", // Private user pages
          "/withdrawal",
          "/deposit/*",
          "/transaction",
        ],
      },
    ],
    sitemap: `${baseUrl}/sitemap.xml`,
  };
}
